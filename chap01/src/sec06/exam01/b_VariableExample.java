package sec06.exam01;

public class b_VariableExample {

	public static void main(String[] args) {
		//싱글라인
		/*멀티라인*/
		
		
		//산술수행
		int value = 10;	//변수선언 & 초기화 (초기값: 10)   =의 의미 : 오른쪽의 값을 복사하여 왼쪽에 대입한다.
		int result= value + 10;
		
		System.out.println(result);

		//빨간줄이 뜨는 경우 - 컴파일에러
		//빨간줄은 안 뜨지만 실행 시 에러가 나는걸 - 런타임에러
	}

}
